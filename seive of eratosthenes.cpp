void storePrime()
{
    int n = 1e6;
    isPrime[0] = isPrime[1] = false;
    for(int i = 2; i*i<=n; i++)
    {
        if(isPrime[i])
        {
            for(int j = i*i; j <= n; j += i)
            {
                isPrime[j] = false;
            }
        }
    }
}
